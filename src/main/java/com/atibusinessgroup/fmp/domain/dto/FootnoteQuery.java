package com.atibusinessgroup.fmp.domain.dto;

public class FootnoteQuery {
	
	private String cxr;
	private String fareAddon;
	private String tarNo;
	private String tarCd;
	private String ftnt;
	private String fareAddonCount;
	private String catNo;
	
	public String getCxr() {
		return cxr;
	}
	public void setCxr(String cxr) {
		this.cxr = cxr;
	}
	public String getFareAddon() {
		return fareAddon;
	}
	public void setFareAddon(String fareAddon) {
		this.fareAddon = fareAddon;
	}
	public String getTarNo() {
		return tarNo;
	}
	public void setTarNo(String tarNo) {
		this.tarNo = tarNo;
	}
	public String getTarCd() {
		return tarCd;
	}
	public void setTarCd(String tarCd) {
		this.tarCd = tarCd;
	}
	public String getFtnt() {
		return ftnt;
	}
	public void setFtnt(String ftnt) {
		this.ftnt = ftnt;
	}
	public String getFareAddonCount() {
		return fareAddonCount;
	}
	public void setFareAddonCount(String fareAddonCount) {
		this.fareAddonCount = fareAddonCount;
	}
	public String getCatNo() {
		return catNo;
	}
	public void setCatNo(String catNo) {
		this.catNo = catNo;
	}
	
	
	
}
