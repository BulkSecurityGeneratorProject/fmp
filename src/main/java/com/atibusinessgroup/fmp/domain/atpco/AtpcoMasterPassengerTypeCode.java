package com.atibusinessgroup.fmp.domain.atpco;

import java.io.Serializable;

import org.springframework.data.annotation.Id;
import org.springframework.data.mongodb.core.mapping.Document;
import org.springframework.data.mongodb.core.mapping.Field;

/**
 * A Passenger Type Code Master.
 */
@Document(collection = "m_ptc_codes")
public class AtpcoMasterPassengerTypeCode implements Serializable {

    private static final long serialVersionUID = 1L;

    @Id
    private String id;

    @Field("ptc")
    private String code;

    @Field("description")
    private String name;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		AtpcoMasterPassengerTypeCode other = (AtpcoMasterPassengerTypeCode) obj;
		if (id == null) {
			if (other.id != null) {
				return false;
			}
		} else if (!id.equals(other.id)) {
			return false;
		}
		return true;
	}

	@Override
	public String toString() {
		return "AtpcoMasterPassengerTypeCode [id=" + id + ", code=" + code + ", name=" + name + "]";
	}
}
